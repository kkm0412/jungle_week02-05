#1*N으로 생각하면 결국 1칸넣냐 2칸넣냐의 차이임.
#dp[i]의 값은 i꺄지의 위치에서 놓을수 있는 가짓수

n = int(input())

dp= [0] * (n+1)
dp[0] = 1
for i in range(1, n+1): #i가 현재위치
    if i-1 >=0:
        dp[i] += dp[i-1]
    if i-2 >= 0:
        dp[i] += dp[i-2]

print(dp[n] % 10007)